class Kata {
    static getVolumeOfCuboid(l, w, h) {
        return l * w * h
    }
}

// Tests

console.log(Kata.getVolumeOfCuboid(1, 2, 2)); // 4
console.log(Kata.getVolumeOfCuboid(6, 2, 5)); // 60

